package app0604.tree.xml;

//한마리의 반려동물을 담을 VO 정의
public class Pet {
	private String type;
	private String name;
	private int age;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
